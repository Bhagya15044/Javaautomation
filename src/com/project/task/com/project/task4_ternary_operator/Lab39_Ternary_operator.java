package com.project.task4_ternary_operator;

public class Lab39_Ternary_operator
{
   // Ternary operator syntax---> ?:
    public static void main(String[] args)
    {
          int age = 24;
          String can_I_Go_To_Goa = (age>18)?("you can go to goa"):("you can't go to goa");
        System.out.println(can_I_Go_To_Goa);
    }
}

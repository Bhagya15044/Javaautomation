package com.project.task7_switch_statement;

public class Lab70_interview_switch_statement
{
    public static void main(String[] args)
    {
        char code = 'C';
        switch (code)
        {
            default:
                System.out.println("Hellooooo");
                break;

            case 'A':
                System.out.println("65");
                break;

            case 'B':
                System.out.println("66");
                break;
        }
    }
}

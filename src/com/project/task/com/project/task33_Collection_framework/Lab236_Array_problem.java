public class Lab236_Array_problem
{
    public static void main(String[] args)
    {
        int [] arr = new int[5];
        arr[0] = 11;
        arr[1] = 12;
        arr[2] = 13;
        arr[3] = 14;
        arr[4] = 15;
    }

    /*
          Array problems :
          --------------
          1. Fixed Size
          2. Same data type is allowed to store the elements
          3. Wastage of memory for 9 elements
          4. Insertion and Deletion is a heavy operation.

          ** To overcome the array problems java guys introduced Collection framework
          ** ArrayList is an upgraded version of array.
     */
}

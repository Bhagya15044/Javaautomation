package com.project.task2_operators;

public class Lab13_Arithemetic_operators
{
    /*
         Arithemetic operators

         + --> addition
         - --> substraction
         * --> multiplication
         / --> division
         % --> modulus
     */
    public static void main(String[] args)
    {
        int a = 20;
        int b= 3;
        float c = 3.0f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b); // it gives quotient as output
        System.out.println(a%b);  // it gives reminder as output
        System.out.println(a/c);  // it gives float value as output because it divides int/float.

    }


}

package com.project.task22_polymorphism.method_overloading;

public class Lab181_math_operations
{
    int add(int a , int b)
    {
        return  a + b;
    }
    int add(int a , int b, int c)
    {
        return  a + b + c;
    }
    double add( double a , double b)
    {
        return a + b;
    }
}

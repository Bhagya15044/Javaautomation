package com.project.task8_for_loop;

public class Lab76_for_loop_no_condition
{
    /*
        as there is no condition i value will keep on increasing
        Process finished with exit code 130 -> execution will be terminated by keyboard
         Process finished with exit code 0 --> code executed successfully and terminated
     */

    public static void main(String[] args)
    {
        for(int i=0 ; ; i++)
        {
            System.out.println(i);
        }
    }
}

package com.project.task8_for_loop;

public class Lab72_for_loop
{
    /*
         for loop is used to help you to repeat a block of code
         i--> initialization, c-->condition, u-->updation(increment/decrement of i value)
     */
    public static void main(String[] args)
    {

        for(int i=0;i<10;i++)
        {
            System.out.println(i); // printed 10 times, values of i is 0 to 9
        }
    }
}

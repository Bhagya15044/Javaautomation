package com.project.task8_for_loop;

public class Lab80_for_loop_float_long
{
    public static void main(String[] args)
    {
        for(long l = 1l ; l>15 ; l++)
        {
            System.out.println(l);
        }

        for(float f = 0.0f ; f>10.67 ; f++)
        {
            System.out.println(f);
        }
    }
}

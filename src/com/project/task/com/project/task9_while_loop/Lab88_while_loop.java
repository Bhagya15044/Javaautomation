package com.project.task9_while_loop;

public class Lab88_while_loop
{
    public static void main(String[] args)
    {
        int i = 0; // initialization will be always outside
        while (i<10) // first it will check the condition then only enters the loop
        {
            System.out.println(i); // print 10 times , i values are from 0 to 9
            i++;
        }
    }
}

package com.project.task10_Do_while_loop;

public class Lab96_do_while_loop
{
    public static void main(String[] args)
    {
        int i = 1;
        do
        {
            System.out.println("I'm the body, i will execute once!!");
            i++; // even the condition is false it will print once
        }while (i<=10);

    }
}
